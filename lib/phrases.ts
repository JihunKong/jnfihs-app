export function getCategoryById(id: string): PhraseCategory | undefined {
  return phraseCategories.find((cat) => cat.id === id);
}

export function searchPhrases(query: string): Phrase[] {
  const loweredQuery = query.toLowerCase();
  return phraseCategories.flatMap((cat) =>
    cat.phrases.filter(
      (phrase) =>
        phrase.ko.includes(query) ||
        phrase.mn.toLowerCase().includes(loweredQuery) ||
        phrase.ru.toLowerCase().includes(loweredQuery) ||
        phrase.vi.toLowerCase().includes(loweredQuery)
    )
  );
}
